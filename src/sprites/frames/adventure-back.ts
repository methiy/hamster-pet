import type { AnimationDef, RLEFrame, RLERow } from '../types'

/** Shift all non-transparent pixels horizontally (positive=right, negative=left), clipping at edges */
function shiftHorizontal(frame: RLEFrame, offset: number): RLEFrame {
  if (offset === 0) return frame
  if (Math.abs(offset) >= 48) return emptyFrame
  return frame.map(row => {
    const pixels: number[] = []
    for (const [count, color] of row) {
      for (let i = 0; i < count; i++) pixels.push(color)
    }
    const shifted: number[] = new Array(48).fill(0)
    for (let i = 0; i < 48; i++) {
      const newPos = i + offset
      if (newPos >= 0 && newPos < 48) {
        shifted[newPos] = pixels[i]
      }
    }
    const rle: RLERow = []
    let runStart = 0
    for (let i = 1; i <= 48; i++) {
      if (i < 48 && shifted[i] === shifted[i - 1]) continue
      rle.push([i - runStart, shifted[runStart]])
      runStart = i
    }
    return rle
  })
}

